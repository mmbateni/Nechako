# PART 4c: KENDALL-BASED JOINT RETURN PERIOD — EXPANDED BIVARIATE COPULA
#
# IMPROVEMENTS over prior version:
#   Marginals  – 4 candidates each:
#     Duration : Gamma, Weibull, Exponential, Log-logistic (manual d/p)
#     Severity : Gamma, Lognormal, Weibull, Log-logistic
#   Copulas    – all 5 Archimedean families in the 'copula' package,
#                each with analytically closed K_C(t):
#     Clayton (lower-tail), Gumbel (upper-tail), Frank (symmetric),
#     Joe (upper-tail, stronger than Gumbel), AMH (symmetric, moderate tau)
#   K_C formula – unified:  K_C(t) = t - iPsi(cop,t)/diPsi(cop,t)
#                 (Genest-Rivest; iPsi = Nelson generator phi, diPsi = phi')
#   Selection  – best marginal pair AND best copula chosen jointly by AIC;
#                full 5-copula x 2-marginal-best sensitivity table reported.
#   Bootstrap  – 500 reps; copula init via iTau for stability in low-tau regime.
#
# References:
#   Salvadori, De Michele & Durante (2011) HESS 15:3293-3305
#   Salvadori, Durante & De Michele  (2013) WRR  49:2308-2311
#   Shiau (2006) Water Resour. Manag. 20:795-815
#
# Outputs (RANKING_DIR):
#   Fig3_MS_Drought_Severity_Ranking_KendallRP.pdf/.png  (updated Fig 3)
#   Fig4c_Copula_Diagnostics.pdf                         (6-panel diagnostic)
####################################################################################
cat("\n══════════════════════════════════════\n")
cat("PART 4c: Kendall RP — expanded copula & marginals\n")
cat("══════════════════════════════════════\n")

kendall_rp_result <- tryCatch({
  
  # ── 0. Log-logistic distribution (not in base R; defined manually) ───────────
  # F(x) = 1 / (1 + (scale/x)^shape),  x,shape,scale > 0
  dllogis_fn <- function(x, shape, scale) {
    ifelse(x <= 0, 0,
           (shape / scale) * (x / scale)^(shape - 1) / (1 + (x / scale)^shape)^2)
  }
  pllogis_fn <- function(q, shape, scale) {
    ifelse(q <= 0, 0, 1 / (1 + (scale / q)^shape))
  }
  
  # ── 1. Event data ─────────────────────────────────────────────────────────────
  ev_cop <- ms_events %>%
    dplyr::filter(duration_class != "D1-2 (Sub-threshold)",
                  !is.na(duration_months), !is.na(severity),
                  is.finite(severity), severity > 0)
  n_ev  <- nrow(ev_cop)
  mu_ia <- n_years_record / n_ev
  cat(sprintf("  Events for copula fit: %d  |  mean inter-arrival: %.2f yr\n",
              n_ev, mu_ia))
  
  dur_vec <- as.numeric(ev_cop$duration_months)
  sev_vec <- ev_cop$severity
  
  # Empirical Kendall's tau (used for copula initialisation)
  tau_emp <- cor(dur_vec, sev_vec, method = "kendall")
  cat(sprintf("  Empirical Kendall tau (duration, severity): %.4f\n", tau_emp))
  
  # ── 2. Fit marginal distributions ────────────────────────────────────────────
  fit_marg <- function(x, dist, start = NULL) {
    tryCatch(
      fitdistrplus::fitdist(x, dist,
                            start  = start,
                            method = "mle"),
      error = function(e) NULL
    )
  }
  
  # --- 2a. Duration ---
  cat("  Fitting duration marginals...\n")
  fits_dur <- list(
    gamma = fit_marg(dur_vec, "gamma"),
    weibull = fit_marg(dur_vec, "weibull"),
    exp = fit_marg(dur_vec, "exp"),
    llogis = tryCatch(
      fitdistrplus::fitdist(dur_vec, "llogis_fn",
                            start = list(shape = 2, scale = median(dur_vec)),
                            ddist = dllogis_fn, pdist = pllogis_fn),
      error = function(e) NULL)
  )
  fits_dur <- Filter(Negate(is.null), fits_dur)
  
  aic_dur  <- sapply(fits_dur, function(f) f$aic)
  best_dur <- names(which.min(aic_dur))
  cat("  Duration AIC:"); for (nm in names(aic_dur)) cat(sprintf("  %s=%.1f", nm, aic_dur[nm])); cat("\n")
  cat(sprintf("  Best duration marginal: %s (AIC=%.1f)\n", best_dur, aic_dur[best_dur]))
  
  # --- 2b. Severity ---
  cat("  Fitting severity marginals...\n")
  fits_sev <- list(
    gamma = fit_marg(sev_vec, "gamma"),
    lnorm = fit_marg(sev_vec, "lnorm"),
    weibull = fit_marg(sev_vec, "weibull"),
    llogis = tryCatch(
      fitdistrplus::fitdist(sev_vec, "llogis_fn",
                            start = list(shape = 2, scale = median(sev_vec)),
                            ddist = dllogis_fn, pdist = pllogis_fn),
      error = function(e) NULL)
  )
  fits_sev <- Filter(Negate(is.null), fits_sev)
  
  aic_sev  <- sapply(fits_sev, function(f) f$aic)
  best_sev <- names(which.min(aic_sev))
  cat("  Severity AIC:"); for (nm in names(aic_sev)) cat(sprintf("  %s=%.1f", nm, aic_sev[nm])); cat("\n")
  cat(sprintf("  Best severity marginal: %s (AIC=%.1f)\n", best_sev, aic_sev[best_sev]))
  
  # ── 3. PIT helper ─────────────────────────────────────────────────────────────
  # Converts raw values to uniform pseudo-observations using fitted marginals.
  pit_vals <- function(x, fit, dist) {
    p <- switch(dist,
                gamma   = pgamma(x, fit$estimate["shape"], fit$estimate["rate"]),
                weibull = pweibull(x, fit$estimate["shape"], fit$estimate["scale"]),
                exp     = pexp(x, fit$estimate["rate"]),
                lnorm   = plnorm(x, fit$estimate["meanlog"], fit$estimate["sdlog"]),
                llogis   = pllogis_fn(x, fit$estimate["shape"], fit$estimate["scale"])
    )
    eps <- 1e-4
    pmax(eps, pmin(1 - eps, p))
  }
  
  u_obs  <- pit_vals(dur_vec, fits_dur[[best_dur]], best_dur)
  v_obs  <- pit_vals(sev_vec, fits_sev[[best_sev]], best_sev)
  uv_mat <- cbind(u_obs, v_obs)
  
  # Target event PIT
  target_ev  <- ms_events %>%
    dplyr::filter(is_recent) %>%
    dplyr::slice_max(severity, n = 1) %>%
    dplyr::slice_head(n = 1)
  target_dur <- as.numeric(target_ev$duration_months[1])
  target_sev <- target_ev$severity[1]
  cat(sprintf("  Target event: duration=%.0f mo, severity=%.3f\n", target_dur, target_sev))
  
  u0 <- pit_vals(target_dur, fits_dur[[best_dur]], best_dur)
  v0 <- pit_vals(target_sev, fits_sev[[best_sev]], best_sev)
  cat(sprintf("  PIT: u0=%.4f (duration), v0=%.4f (severity)\n", u0, v0))
  
  # ── 4. Unified Kendall distribution function (Genest-Rivest formula) ─────────
  # K_C(t) = t - phi(t) / phi'(t)
  # where phi(t) = iPsi(cop, t)  [Nelson generator]
  #       phi'(t) = diPsi(cop, t) [derivative of Nelson generator]
  # This is exact for ALL Archimedean copulas in the 'copula' package.
  kc_arch <- function(t, cop) {
    t <- pmax(1e-9, pmin(1 - 1e-9, t))
    phi_t  <- copula::iPsi(cop,  t)
    dphi_t <- copula::diPsi(cop, t)
    t - phi_t / dphi_t
  }
  
  # ── 5. Fit all 5 Archimedean copulas; rank by AIC ────────────────────────────
  # Initialise each copula at the tau-matching parameter for stability
  # (avoids MLE divergence in the low-tau regime).
  tau_init <- pmax(0.01, tau_emp)   # guard against negative tau for families that require >0
  
  safe_fit_cop <- function(cop_template, uv) {
    tryCatch({
      th_init  <- copula::iTau(cop_template, tau_init)
      cop_init <- copula::setTheta(cop_template, th_init)
      copula::fitCopula(cop_init, uv, method = "ml")
    }, error = function(e) NULL)
  }
  
  cop_candidates <- list(
    Clayton = copula::claytonCopula(dim = 2L),
    Gumbel  = copula::gumbelCopula(dim  = 2L),
    Frank   = copula::frankCopula(dim   = 2L),
    Joe     = copula::joeCopula(dim     = 2L),
    AMH     = copula::amhCopula(dim     = 2L)
  )
  
  cop_fits <- lapply(cop_candidates, safe_fit_cop, uv = uv_mat)
  cop_fits <- Filter(Negate(is.null), cop_fits)
  
  aic_cop  <- sapply(cop_fits, AIC)
  best_cop_name <- names(which.min(aic_cop))
  best_fit      <- cop_fits[[best_cop_name]]
  best_cop      <- best_fit@copula
  theta_hat     <- best_cop@parameters
  tau_fitted    <- copula::tau(best_cop)
  
  cat("\n  Copula AIC ranking:\n")
  for (nm in names(sort(aic_cop))) {
    th <- cop_fits[[nm]]@copula@parameters
    tk <- copula::tau(cop_fits[[nm]]@copula)
    marker <- if (nm == best_cop_name) " <-- selected" else ""
    cat(sprintf("    %-8s theta=%.4f  tau=%.4f  AIC=%7.2f%s\n",
                nm, th, tk, aic_cop[nm], marker))
  }
  
  # ── 6. Kendall RP for the target event ───────────────────────────────────────
  t0       <- copula::pCopula(c(u0, v0), best_cop)
  Kc0      <- pmin(kc_arch(t0, best_cop), 1 - 1e-9)
  T_kendall <- mu_ia / (1 - Kc0)
  
  cat(sprintf("\n  C(u0,v0)=%.4f  K_C(t*)=%.4f  T_Kendall=%.0f yr\n",
              t0, Kc0, T_kendall))
  
  # ── 6b. Sensitivity table: T_K across all fitted copulas ─────────────────────
  cat("\n  Sensitivity — T_K for all copulas (same best marginals):\n")
  sens_df <- do.call(rbind, lapply(names(cop_fits), function(nm) {
    cf   <- cop_fits[[nm]]
    cop  <- cf@copula
    t0_s <- copula::pCopula(c(u0, v0), cop)
    Kc_s <- pmin(kc_arch(t0_s, cop), 1 - 1e-9)
    TK_s <- mu_ia / (1 - Kc_s)
    data.frame(Copula = nm,
               theta  = round(cop@parameters, 4),
               tau    = round(copula::tau(cop), 4),
               t_star = round(t0_s, 4),
               K_C    = round(Kc_s, 5),
               T_K_yr = round(TK_s, 0))
  }))
  print(sens_df, row.names = FALSE)
  
  # ── 7. Bootstrap 95 % CI — 500 reps, tau-initialised copula refitting ────────
  N_BOOT <- 500L
  set.seed(42L)
  cat(sprintf("\n  Bootstrapping CI (%d reps)...\n", N_BOOT))
  
  refit_marginal <- function(x, dist, old_fit) {
    tryCatch({
      if (dist == "llogis") {
        fitdistrplus::fitdist(x, "llogis_fn",
                              start = list(shape = old_fit$estimate["shape"],
                                           scale = old_fit$estimate["scale"]),
                              ddist = dllogis_fn, pdist = pllogis_fn)
      } else {
        fitdistrplus::fitdist(x, dist, method = "mle")
      }
    }, error = function(e) old_fit)   # keep original fit if refit fails
  }
  
  T_boot <- vapply(seq_len(N_BOOT), function(b) {
    tryCatch({
      idx_b  <- sample(n_ev, n_ev, replace = TRUE)
      db     <- dur_vec[idx_b]; sb <- sev_vec[idx_b]
      fd_b   <- refit_marginal(db, best_dur, fits_dur[[best_dur]])
      fs_b   <- refit_marginal(sb, best_sev, fits_sev[[best_sev]])
      ub     <- pit_vals(db, fd_b, best_dur)
      vb     <- pit_vals(sb, fs_b, best_sev)
      u0b    <- pit_vals(target_dur, fd_b, best_dur)
      v0b    <- pit_vals(target_sev, fs_b, best_sev)
      tau_b  <- pmax(0.01, cor(db, sb, method = "kendall"))
      th_b   <- copula::iTau(best_cop, tau_b)
      cop_b  <- copula::setTheta(best_cop, th_b)
      fc_b   <- copula::fitCopula(cop_b, cbind(ub, vb), method = "ml")
      cb     <- fc_b@copula
      t0b    <- copula::pCopula(c(u0b, v0b), cb)
      Kcb    <- pmin(kc_arch(t0b, cb), 1 - 1e-9)
      mu_ia  / (1 - Kcb)
    }, error = function(e) NA_real_)
  }, FUN.VALUE = numeric(1L))
  
  T_boot_fin <- T_boot[is.finite(T_boot) & T_boot < 1e7]  # cap extreme outliers
  ci_lo  <- quantile(T_boot_fin, 0.025, na.rm = TRUE)
  ci_hi  <- quantile(T_boot_fin, 0.975, na.rm = TRUE)
  ci_med <- quantile(T_boot_fin, 0.500, na.rm = TRUE)
  n_conv <- length(T_boot_fin)
  
  cat(sprintf("  T_K = %.0f yr  |  95%% CI: [%.0f, %.0f] yr  |  median %.0f yr\n",
              T_kendall, ci_lo, ci_hi, ci_med))
  cat(sprintf("  (%d/%d reps converged; %d capped at 1e7)\n",
