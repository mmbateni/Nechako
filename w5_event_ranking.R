# PART 3c: COMPARISON — SW08 (this script) vs HYSTERESIS (w1 catalogs)
#
# Loads the w1-generated basin_event_catalog CSVs (hysteresis method) for each
# available index and computes the same duration-class summary.  The two tables
# are then joined and saved as Table_SW08_vs_Hyst_Comparison.csv so the
# manuscript Supplementary can document how definition choice affects event counts,
# mean duration, mean intensity, and mean severity.
#
# If w1 catalogs are absent (e.g. first run before w1), this part is skipped
# gracefully.
####################################################################################
cat("\n══════════════════════════════════════\n")
cat("PART 3c: SW08 vs Hysteresis Comparison\n")
cat("══════════════════════════════════════\n")

tryCatch({
  # ── Load hysteresis catalogs from w1 ─────────────────────────────────────
  hyst_events_list <- list()
  for (idx_info in INDICES_TO_ANALYZE) {
    for (sc in idx_info$scales) {
      lab   <- sprintf("%s-%02d", toupper(idx_info$index), sc)
      fpath <- file.path(TREND_DIR, "basin_extent",
                         sprintf("%s_basin_event_catalog.csv", lab))
      if (!file.exists(fpath)) next
      hdf <- tryCatch(read.csv(fpath, stringsAsFactors = FALSE), error = function(e) NULL)
      if (is.null(hdf) || nrow(hdf) == 0) next
      hdf$start_date <- as.Date(hdf$start_date)
      hdf$end_date   <- as.Date(hdf$end_date)
      # w1 catalog column: mean_severity = mean(|min(x,0)|) [deficit below 0]
      # intensity = mean_severity (already stored); severity = mean_severity × duration
      if ("mean_severity" %in% names(hdf) && !"mean_int" %in% names(hdf))
        hdf$mean_int <- as.numeric(hdf$mean_severity)
      if (!"severity" %in% names(hdf))
        hdf$severity <- hdf$mean_int * as.integer(hdf$duration_months)
      hdf$duration_class <- classify_duration(as.integer(hdf$duration_months))
      hdf$index_label    <- sprintf("%s_%02d", idx_info$index, sc)
      hyst_events_list[[lab]] <- hdf
    }
  }
  
  if (length(hyst_events_list) == 0L) {
    cat("  ⚠ No w1 hysteresis catalogs found — comparison skipped\n")
  } else {
    hyst_all <- dplyr::bind_rows(hyst_events_list)
    hyst_all <- hyst_all %>%
      dplyr::filter(start_year >= HISTORICAL_START,
                    start_year <= HISTORICAL_END,
                    duration_class != "D1-2 (Sub-threshold)")
    
    # Summarise hysteresis
    hyst_sum <- hyst_all %>%
      dplyr::group_by(index_label, duration_class) %>%
      dplyr::summarise(
        n_events_hyst    = dplyr::n(),
        mean_dur_hyst    = round(mean(as.integer(duration_months), na.rm = TRUE), 1),
        mean_I_hyst      = round(mean(mean_int,  na.rm = TRUE), 5),
        mean_S_hyst      = round(mean(severity,  na.rm = TRUE), 4),
        rp_hyst          = round(n_years_record / dplyr::n(), 1),
        .groups = "drop"
      )
    
    # Summarise SW08 (from master_events built in Part 1)
    sw08_sum <- master_events %>%
      dplyr::filter(duration_class != "D1-2 (Sub-threshold)") %>%
      dplyr::group_by(index_label, duration_class) %>%
      dplyr::summarise(
        n_events_sw08  = dplyr::n(),
        mean_dur_sw08  = round(mean(duration_months, na.rm = TRUE), 1),
        mean_I_sw08    = round(mean(mean_int,        na.rm = TRUE), 5),
        mean_S_sw08    = round(mean(severity,        na.rm = TRUE), 4),
        rp_sw08        = round(n_years_record / dplyr::n(), 1),
        .groups = "drop"
      )
    
    comp_tbl <- dplyr::full_join(sw08_sum, hyst_sum,
                                 by = c("index_label", "duration_class")) %>%
      dplyr::mutate(
        delta_n_events  = n_events_sw08  - n_events_hyst,
        delta_mean_dur  = round(mean_dur_sw08  - mean_dur_hyst, 1),
        delta_mean_I    = round(mean_I_sw08    - mean_I_hyst, 5),
        delta_mean_S    = round(mean_S_sw08    - mean_S_hyst, 4),
        duration_class  = factor(duration_class,
                                 levels = c("D3-6 (Short-term)",
                                            "D7-12 (Medium-term)",
                                            "D12+ (Long-term)"))
      ) %>%
      dplyr::arrange(index_label, duration_class)
    
    write.csv(comp_tbl,
              file.path(RANKING_DIR, "Table_SW08_vs_Hyst_Comparison.csv"),
              row.names = FALSE)
    cat(sprintf("✓ Saved: Table_SW08_vs_Hyst_Comparison.csv (%d rows)\n", nrow(comp_tbl)))
    
    # Print compact summary to console
    cat("\n  SW08 vs Hysteresis — key differences (SPEI-06 example):\n")
    ex <- comp_tbl %>% dplyr::filter(index_label == "spei_03")
    if (nrow(ex) > 0) {
      cat("  Duration class       | n SW08 | n Hyst | dN | mean_I_SW08 | mean_I_Hyst\n")
      cat("  ---------------------|--------|--------|----|-------------|------------\n")
      for (i in seq_len(nrow(ex))) {
        cat(sprintf("  %-20s | %6d | %6d | %+3d | %11.5f | %11.5f\n",
                    as.character(ex$duration_class[i]),
                    ex$n_events_sw08[i]  %||% NA,
                    ex$n_events_hyst[i]  %||% NA,
                    ex$delta_n_events[i] %||% NA,
                    ex$mean_I_sw08[i]    %||% NA,
                    ex$mean_I_hyst[i]    %||% NA))
      }
    }
    cat("\n  NOTE: mean_I_Hyst = deficit below 0.0; mean_I_SW08 = deficit below -1.0\n")
    cat("  Lower mean_I_SW08 is expected — events measured from a stricter baseline.\n")
  }
}, error = function(e) {
  cat(sprintf("  ⚠ Part 3c failed: %s\n", e$message))
})

