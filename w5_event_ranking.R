#
# EVENT DEFINITION — Sheffield & Wood (2008) [SW08], applied to SPEI/SPI:
#   Onset       : index < DROUGHT_ONSET  (= -1.0)
#   Termination : index >= DROUGHT_ONSET (same single threshold — NO hysteresis)
#   Intensity   : I = mean(DROUGHT_ONSET − x_t) over event months      [≥ 0]
#                 i.e. mean deficit below −1.0, not below 0.0
#   Severity    : S = I × D
#   Min duration: NONE — all events ≥ 1 month are retained
#   Duration classes: D3-6 (Short-term), D7-12 (Medium-term), D12+ (Long-term)
#
#   This is the faithful SW08 definition adapted to standardised indices.
#   Key difference from the previous hysteresis version (end_thr = 0.0):
#     • Events split whenever the index briefly rises above −1.0, producing
#       more, shorter events with higher intensity (measured from −1.0, not 0.0).
#     • A comparison table (Part 3b) documents the old vs new event statistics.
#
#   References:
#     Sheffield & Wood (2008) Clim Dyn 31:79-105 — duration classes, I×D severity
#     Yevjevich (1967) — threshold-level run method (theoretical basis)
#
# MODIFICATIONS :
#    Part 0  — build_sw08_catalog(): reads raw NetCDF files,
#                            computes area-weighted basin average, applies SW08
#                            event loop; replaces load_event_catalog().
#    Part 1  — catalogs now built on-the-fly (no w1 dependency).
