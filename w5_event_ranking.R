# ── Drought event identification parameters ───────────────────────────────────
# Threshold: −0.5 corresponds to the 31st percentile of the standard normal,
#   the most common climatological drought onset threshold (Lloyd-Hughes &
#   Saunders 2002; Spinoni et al. 2014, 2015).  It is more conservative (flags
#   more months) than the McKee et al. (1993) −1.0 threshold (16th percentile)
#   and corresponds to the lower tercile of the precipitation distribution.
#   Note: the SW08 10th-percentile threshold translates to −1.28 on a standard
#   normal scale, which is distinct from both −1.0 and −0.5.
DROUGHT_ONSET_EVENT <- -0.5   # onset = termination (single threshold, no hysteresis)

# ── Scale-specific minimum duration (months) ──────────────────────────────────
# Rationale: at the liberal −0.5 threshold, ~31 % of months qualify by
#   construction.  Without a minimum duration filter, noise-driven single-month
#   exceedances dominate the catalog.  Two criteria govern the choice:
#     (1) Accumulation-window criterion: min_dur >= scale, so no event can be
#         generated by a single anomalous month propagated by temporal smoothing.
#     (2) Noise-floor criterion: for SPI/SPEI-1 (near-white-noise), the
#         literature minimum of 3 months (Lloyd-Hughes & Saunders 2002) applies.
#   The binding constraint is max(criterion_1, criterion_2) = scale for scale>=3,
#   and 3 months for scale = 1.
#   Consequence: at scales ≥ 6 the D3-6 (Short-term) class is structurally
#   absent from the catalog; all events are D7-12 or D12+.  This is explicitly
#   acknowledged in the manuscript (Section 3.2).
MIN_DURATION_MAP <- list(
  "1"  = 1L,   # SPI/SPEI-1 : min = k = 1 month (trivial; manuscript Section 3.2.2)
  "2"  = 2L,   # SPI/SPEI-2 : min = k = 2 months
  "3"  = 3L,   # SPI/SPEI-3 : min = k = 3 months = one full accumulation window
  "6"  = 6L,   # SPI/SPEI-6 : one full accumulation window (stricter than w1 Hyst = 4m)
  "9"  = 9L,   # SPI/SPEI-9 : one full accumulation window
  "12" = 12L   # SPI/SPEI-12: one full accumulation window = full annual cycle
)

#' Return scale-specific minimum event duration
get_event_min_duration <- function(scale) {
  key <- as.character(as.integer(scale))
  val <- MIN_DURATION_MAP[[key]]
  if (!is.null(val)) as.integer(val) else as.integer(scale)   # default = scale
}

