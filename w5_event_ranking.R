# ── Helper: re-usable log-logistic d/p (defined in Part 4c; re-declare here
#    in case Part 4c was skipped or its tryCatch silently failed) ─────────────
if (!exists("dllogis_fn")) {
  dllogis_fn <- function(x, shape, scale)
    ifelse(x <= 0, 0,
           (shape/scale)*(x/scale)^(shape-1)/(1+(x/scale)^shape)^2)
  pllogis_fn <- function(q, shape, scale)
    ifelse(q <= 0, 0, 1/(1+(scale/q)^shape))
}

# ── Helper: run full Kendall RP analysis for ONE index ───────────────────────
run_kendall_rp <- function(idx_label, n_boot = 500L) {
  tryCatch({
    
    ev <- master_events %>%
      dplyr::filter(index_label == idx_label,
                    duration_class != "D1-2 (Sub-threshold)",
                    !is.na(duration_months), !is.na(severity),
                    is.finite(severity), severity > 0)
    if (nrow(ev) < 8) stop(sprintf("too few events (%d) for copula fitting", nrow(ev)))
    
    n_ev   <- nrow(ev)
    mu_ia  <- n_years_record / n_ev
    dur_v  <- as.numeric(ev$duration_months)
    sev_v  <- ev$severity
    tau_e  <- cor(dur_v, sev_v, method = "kendall")
    eps    <- 1e-4
    
    # ── Marginal fitting ───────────────────────────────────────────────────
    fit_m <- function(x, dist, s = NULL) {
      tryCatch(fitdistrplus::fitdist(x, dist, start = s, method = "mle"),
               error = function(e) NULL)
    }
    fd <- Filter(Negate(is.null), list(
      gamma   = fit_m(dur_v, "gamma"),
      weibull = fit_m(dur_v, "weibull"),
      exp     = fit_m(dur_v, "exp"),
      llogis  = tryCatch(fitdistrplus::fitdist(dur_v, "llogis_fn",
                                               start = list(shape=2, scale=median(dur_v)),
                                               ddist=dllogis_fn, pdist=pllogis_fn), error=function(e) NULL)
    ))
    fs <- Filter(Negate(is.null), list(
      gamma   = fit_m(sev_v, "gamma"),
      lnorm   = fit_m(sev_v, "lnorm"),
      weibull = fit_m(sev_v, "weibull"),
      llogis  = tryCatch(fitdistrplus::fitdist(sev_v, "llogis_fn",
                                               start = list(shape=2, scale=median(sev_v)),
                                               ddist=dllogis_fn, pdist=pllogis_fn), error=function(e) NULL)
    ))
    bd <- names(which.min(sapply(fd, `[[`, "aic")))
    bs <- names(which.min(sapply(fs, `[[`, "aic")))
    
    # ── PIT helper ────────────────────────────────────────────────────────
    pit <- function(x, f, dist) {
      p <- switch(dist,
                  gamma   = pgamma(x, f$estimate["shape"], f$estimate["rate"]),
                  weibull = pweibull(x, f$estimate["shape"], f$estimate["scale"]),
                  exp     = pexp(x, f$estimate["rate"]),
                  lnorm   = plnorm(x, f$estimate["meanlog"], f$estimate["sdlog"]),
                  llogis  = pllogis_fn(x, f$estimate["shape"], f$estimate["scale"])
      )
      pmax(eps, pmin(1-eps, p))
    }
    
    u_obs <- pit(dur_v, fd[[bd]], bd)
    v_obs <- pit(sev_v, fs[[bs]], bs)
    uv    <- cbind(u_obs, v_obs)
    
    # Target 2022-2025 event for this index
    tgt <- master_events %>%
      dplyr::filter(index_label == idx_label, is_recent) %>%
      dplyr::slice_max(severity, n = 1) %>%
      dplyr::slice_head(n = 1)
    if (nrow(tgt) == 0) stop("no recent event found")
    u0 <- pit(as.numeric(tgt$duration_months[1]), fd[[bd]], bd)
    v0 <- pit(tgt$severity[1],                    fs[[bs]], bs)
    
    # ── Unified K_C ───────────────────────────────────────────────────────
    kc <- function(t, cop) {
      t <- pmax(1e-9, pmin(1-1e-9, t))
      pmin(t - copula::iPsi(cop,t) / copula::diPsi(cop,t), 1-1e-9)
    }
    
    # ── Fit 5 copulas ─────────────────────────────────────────────────────
    ti <- pmax(0.01, tau_e)
    safe_cop <- function(tmpl) tryCatch({
      th <- copula::iTau(tmpl, ti)
      copula::fitCopula(copula::setTheta(tmpl, th), uv, method = "ml")
    }, error = function(e) NULL)
    
    cf <- Filter(Negate(is.null), list(
      Clayton = safe_cop(copula::claytonCopula(dim=2L)),
      Gumbel  = safe_cop(copula::gumbelCopula(dim=2L)),
      Frank   = safe_cop(copula::frankCopula(dim=2L)),
      Joe     = safe_cop(copula::joeCopula(dim=2L)),
      AMH     = safe_cop(copula::amhCopula(dim=2L))
    ))
    if (length(cf) == 0) stop("all copula fits failed")
    
    aic_c  <- sapply(cf, AIC)
    bc_nm  <- names(which.min(aic_c))
    bc_cop <- cf[[bc_nm]]@copula
    th_hat <- bc_cop@parameters
    tau_f  <- copula::tau(bc_cop)
    
    t0     <- copula::pCopula(c(u0, v0), bc_cop)
    Kc0    <- kc(t0, bc_cop)
    TK     <- mu_ia / (1 - Kc0)
    
    # ── Bootstrap CI ──────────────────────────────────────────────────────
    set.seed(42L)
    refit_m <- function(x, dist, old_f) tryCatch({
      if (dist == "llogis")
        fitdistrplus::fitdist(x, "llogis_fn",
                              start=list(shape=old_f$estimate["shape"],scale=old_f$estimate["scale"]),
                              ddist=dllogis_fn, pdist=pllogis_fn)
      else fitdistrplus::fitdist(x, dist, method="mle")
    }, error=function(e) old_f)
    
    tb <- vapply(seq_len(n_boot), function(b) {
      tryCatch({
        idx_b <- sample(n_ev, n_ev, replace=TRUE)
        db <- dur_v[idx_b]; sb <- sev_v[idx_b]
        fdb <- refit_m(db, bd, fd[[bd]])
        fsb <- refit_m(sb, bs, fs[[bs]])
        ub  <- pit(db, fdb, bd); vb <- pit(sb, fsb, bs)
        u0b <- pit(as.numeric(tgt$duration_months[1]), fdb, bd)
        v0b <- pit(tgt$severity[1],                    fsb, bs)
        ti_b <- pmax(0.01, cor(db, sb, method="kendall"))
        fc_b <- copula::fitCopula(
          copula::setTheta(bc_cop, copula::iTau(bc_cop, ti_b)),
          cbind(ub, vb), method="ml")
        cb   <- fc_b@copula
        t0b  <- copula::pCopula(c(u0b, v0b), cb)
        mu_ia / (1 - kc(t0b, cb))
      }, error=function(e) NA_real_)
    }, FUN.VALUE=numeric(1L))
    
    tb_fin <- tb[is.finite(tb) & tb < 1e7]
    ci_lo  <- quantile(tb_fin, 0.025, na.rm=TRUE)
    ci_hi  <- quantile(tb_fin, 0.975, na.rm=TRUE)
    
    list(index=idx_label, n_ev=n_ev, tau_emp=round(tau_e,4),
         best_cop=bc_nm, theta=round(th_hat,4), tau_fit=round(tau_f,4),
         best_dur=bd, best_sev=bs,
         u0=round(u0,4), v0=round(v0,4),
         t_star=round(t0,4), Kc=round(Kc0,5),
         TK=round(TK,0), ci_lo=round(ci_lo,0), ci_hi=round(ci_hi,0),
         n_boot_conv=length(tb_fin),
         tgt_dur=tgt$duration_months[1], tgt_sev=round(tgt$severity[1],3),
         mu_ia=round(mu_ia,2),
         aic_cop=aic_c, cf=cf, uv=uv, u0=u0, v0=v0,
         fit_best=cf[[bc_nm]])
  }, error=function(e) {
    list(index=idx_label, error=e$message,
         TK=NA_real_, ci_lo=NA_real_, ci_hi=NA_real_)
  })
}

